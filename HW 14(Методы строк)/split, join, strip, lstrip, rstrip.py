# Split - Разделяет строку ПО ПРОБЕЛАМ, где он встречает пробел, там он его УДАЛЯЕТ, ВСЕ ЧТО БЫЛО ДО ПРОБЕЛА он сохраняет в ОТДЕЛЬНУЮ СТРОКУ и СОЗДАЕТ СПИСОК


a1 = "ivanov ivan ivanovich"
a2 = a1.split()
print(a2)
# ['ivanov', 'ivan', 'ivanovich']


a3 = len(a2)
print(a3)
# 3


a4 = a1.split("n")  # Берет все ДО "n" и создает из этого отдельную строку, затем делает так же с последующими буквами
print(a4)
# ['iva', 'ov iva', ' iva', 'ovich']


b1 = "43,543,765,3,765,432"
b2 = b1.split(",")
print(b2)
# ['43', '543', '765', '3', '765', '432']

# --------------------------------------------------------------------------------------------------------------------------------------------------

b3 = "=".join(b2) # JOIN - в методе JOIN надо сначала указать СИМВОЛ который будет ОБЪЕДИНЯТЬ, затем В СКОБКАХ написать ПЕРЕМЕННУЮ к которой обращаемся
print(b3)
# 43=543=765=3=765=432



hi = "hello".join(b2)
