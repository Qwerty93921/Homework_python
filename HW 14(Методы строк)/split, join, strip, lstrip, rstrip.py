w1 = "    hello     \n" # STRIP метод убирает ПРОБЕЛЫ И ПЕРЕНОС НА НОВУЮ СТРОКУ СЛЕВА И СПРАВА
print(w1)
#     hello     



w2 = w1.strip()
print(w2)
# hello

w3 = w1.rstrip()       # RSTRIP - УБИРАЕТ ТОЛЬКО СПРАВА ПРОБЕЛЫ
print(w3)
#     hello


w4 = w1.lstrip()       # LSTRIP - УБИРАЕТ ТОЛЬКО СЛЕВА ПРОБЕЛЫ
print(w4)
# hello     

